package com.passwordboss.android.beans;

public class SearchDataBean {
	
	private String uuid, type, secureItemTypeName, name, identifier, value, siteFriendlyName, siteImageUrl, siteImageId, siteId;
	private int color;
	
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getSecureItemTypeName() {
		return secureItemTypeName;
	}
	public void setSecureItemTypeName(String secureItemTypeName) {
		this.secureItemTypeName = secureItemTypeName;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIdentifier() {
		return identifier;
	}
	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getSiteFriendlyName() {
		return siteFriendlyName;
	}
	public void setSiteFriendlyName(String siteFriendlyName) {
		this.siteFriendlyName = siteFriendlyName;
	}
	public String getSiteImageUrl() {
		return siteImageUrl;
	}
	public void setSiteImageUrl(String siteImageUrl) {
		this.siteImageUrl = siteImageUrl;
	}
	public String getSiteImageId() {
		return siteImageId;
	}
	public void setSiteImageId(String siteImageId) {
		this.siteImageId = siteImageId;
	}
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	public String getSiteId() {
		return siteId;
	}
	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}
}
